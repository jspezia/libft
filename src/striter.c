void	ft_striter(char *s, void (*f)(char *))
{
	int	i;

	i = 0;
	while (s[i] != '\0')
	{
		f(s);
		s = s + 1;
	}
}

void				ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int	i;

	i = 0;
	while (s[i] != '\0')
	{
		f(i, s);
		s = s + 1;
		i++;
	}
}
